    public void LoadAllData()
    {   
        setcontrols(false);
        setresets();
        Author au =new Author(); // tim tat ca	
        ObjectSet<Author> Aus = db.queryByExample(au);
	for (Author a:Aus) 
	{
            Object[] row={a.getName(), a.getBirthday()};
            tbmodel = (DefaultTableModel) jtbAuthor.getModel();
            tbmodel.addRow(row);  
	}
        jtbAuthor.setRowSelectionInterval(0, 0);
        txttacgia.setText(jtbAuthor.getValueAt(jtbAuthor.getSelectedRow(), 0).toString());
    }
    
