    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int xoa = JOptionPane.showConfirmDialog(null, "delete "+txttacgia.getText(),"Xoa", JOptionPane.YES_NO_OPTION);
        if (xoa == JOptionPane.OK_OPTION)
        {
            try
            {
                Author au = (Author)db.queryByExample(new Author(txttacgia.getText())).next();
                db.delete(au);
                tbmodel.getDataVector().removeAllElements();
                LoadAllData();
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Xoa khong thanh cong, Error:"+ex);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        //Buoc 3.1 Them
        if (luu)
        {
            try
            {
                if ((txttacgia.getText().length() > 255) || (txttacgia.getText().length()<1) )
                {
                    JOptionPane.showMessageDialog(this, "Kiểm tra\nSo luong ky tu nhap");
                    return;
                }
                //---------------------------kết thúc test lỗi
                else
                {
                    String tenTacGia  = txttacgia.getText();
                    //String tenlop=txttenlop.getText();
                    Author TG = new Author(tenTacGia);
                    db.store(TG);
                    JOptionPane.showMessageDialog(this,"Them "+txttacgia.getText());
                    tbmodel.getDataVector().removeAllElements();
                    LoadAllData();
                    jtbAuthor.setRowSelectionInterval(jtbAuthor.getRowCount()-1, jtbAuthor.getRowCount()-1);
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Them khong thanh cong, Error:"+ex);
            }
        }
        //Buoc 4.1 Sua
        else if (!luu)
        {
            try
            {
                //kiểm tra độ dài ký tự nhập va khoang trang
                if ((txttacgia.getText().length() > 255) || (txttacgia.getText().length()<1) )
                {
                    JOptionPane.showMessageDialog(this, "Kiểm tra\nSo luong ky tu nhap");
                    return;
                }
                //---------------------------kết thúc test lỗi
                else
                {                  
                    int index= jtbAuthor.getSelectedRow();  
                    String tenTacGia  = txttacgia.getText();
                    //Author au = (Author) db.queryByExample(new Author(tenTacGia)).next(); // tim trong data co dong tenTacGia
                    
                    Calendar calendar = Calendar.getInstance(); 
                    calendar.set(1990, Calendar.OCTOBER, 10); 
                    
                    auUpdate.setBirthday(calendar.getTime()); 
                    auUpdate.setName(tenTacGia);
                    db.store(auUpdate);
                    
                    tbmodel.getDataVector().removeAllElements();
                    LoadAllData();
                    jtbAuthor.setRowSelectionInterval(index, index);
                    JOptionPane.showMessageDialog(this,"Da cap nhat "+txttacgia.getText());
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Cap nhat khong thanh cong, Error:"+ex);
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        int option = fc.showSaveDialog(this);
        if(option == JFileChooser.APPROVE_OPTION){
            String filename = fc.getSelectedFile().getName(); 
            String path = fc.getSelectedFile().getParentFile().getPath();
            int len = filename.length();
            String ext = "";
            String file = "";
            if(len > 4){
                    ext = filename.substring(len-4, len);
            }
            if(ext.equals(".xls")){
                    file = path + "\\" + filename; 
            }else{
                    file = path + "\\" + filename + ".xls"; 
            }
            lib uselib = new lib();
            uselib.toExcel(jtbAuthor, new File(file));
        }
    }//GEN-LAST:event_btnExportActionPerformed

    
