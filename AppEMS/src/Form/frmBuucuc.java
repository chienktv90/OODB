    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int xoa = JOptionPane.showConfirmDialog(null, "delete "+txtMaBuuCuc.getText(),"Xoa", JOptionPane.YES_NO_OPTION);
        if (xoa == JOptionPane.OK_OPTION)
        {
            try
            {
                Buucuc bc = (Buucuc)db.queryByExample(new Buucuc(txtMaBuuCuc.getText())).next();
                db.delete(bc);
                tbmodel.getDataVector().removeAllElements();
                LoadAllData();
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Xoa khong thanh cong, Error:"+ex);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

