    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        int option = fc.showSaveDialog(this);
        if(option == JFileChooser.APPROVE_OPTION){
            String filename = fc.getSelectedFile().getName(); 
            String path = fc.getSelectedFile().getParentFile().getPath();
            int len = filename.length();
            String ext = "";
            String file = "";
            if(len > 4){
                    ext = filename.substring(len-4, len);
            }
            if(ext.equals(".xls")){
                    file = path + "\\" + filename; 
            }else{
                    file = path + "\\" + filename + ".xls"; 
            }
            lib uselib = new lib();
            uselib.toExcel(jtbBuuCuc, new File(file));
        }
    }//GEN-LAST:event_btnExportActionPerformed
    
    public void LoadAllData()
    {   
        setcontrols(false);
        setresets();
        Buucuc bc =new Buucuc(); // tim tat ca	
        ObjectSet<Buucuc> bcs = db.queryByExample(bc);
	for (Buucuc a:bcs) 
	{
            Object[] row={a.getIdBuuCuc(), a.getTenBuuCuc()};
            tbmodel = (DefaultTableModel) jtbBuuCuc.getModel();
            tbmodel.addRow(row);  
	}
        jtbBuuCuc.setRowSelectionInterval(0, 0);
        txtMaBuuCuc.setText(jtbBuuCuc.getValueAt(jtbBuuCuc.getSelectedRow(), 0).toString());
        txtTenBuuCuc.setText(jtbBuuCuc.getValueAt(jtbBuuCuc.getSelectedRow(), 1).toString());
    }
    
