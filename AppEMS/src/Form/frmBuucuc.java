    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        //Buoc 3.1 Them
        if (luu)
        {
            try
            {
                if ((txtMaBuuCuc.getText().length() > 10) || (txtMaBuuCuc.getText().length()<1) )
                {
                    JOptionPane.showMessageDialog(this, "Kiểm tra\nSo luong ky tu nhap");
                    return;
                }
                else if ((txtTenBuuCuc.getText().length() > 10) || (txtTenBuuCuc.getText().length()<1) )
                {
                    JOptionPane.showMessageDialog(this, "Kiểm tra\nSo luong ky tu nhap");
                    return;
                }
                //---------------------------kết thúc test lỗi
                else
                {
                    String maBuuCuc  = txtMaBuuCuc.getText();
                    String tenBuuCuc = txtTenBuuCuc.getText();
                    String diachi="";
                    Buucuc bc = new Buucuc(maBuuCuc, tenBuuCuc, diachi);
                    
                    db.store(bc);

                    JOptionPane.showMessageDialog(this,"Them "+txtTenBuuCuc.getText());
                    tbmodel.getDataVector().removeAllElements();
                    LoadAllData();
                    jtbBuuCuc.setRowSelectionInterval(jtbBuuCuc.getRowCount()-1, jtbBuuCuc.getRowCount()-1);
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Them khong thanh cong, Error:"+ex);
            }
        }
        //Buoc 4.1 Sua
        else if (!luu)
        {
            
            try
            {
                if ((txtMaBuuCuc.getText().length() > 10) || (txtMaBuuCuc.getText().length()<1) )
                {
                    JOptionPane.showMessageDialog(this, "Kiểm tra\nSo luong ky tu nhap");
                    return;
                }
                else if ((txtTenBuuCuc.getText().length() > 10) || (txtTenBuuCuc.getText().length()<1) )
                {
                    JOptionPane.showMessageDialog(this, "Kiểm tra\nSo luong ky tu nhap");
                    return;
                }
                //---------------------------kết thúc test lỗi
                else
                {                  
                    int index= jtbBuuCuc.getSelectedRow(); 
                    String maBuuCuc  = txtMaBuuCuc.getText();
                    String tenBuuCuc = txtTenBuuCuc.getText();
                    //Author au = (Author) db.queryByExample(new Author(tenTacGia)).next(); // tim trong data co dong tenTacGia
                    
                    bcUpdate.setIdBuuCuc(maBuuCuc);
                    bcUpdate.setTenBuuCuc(tenBuuCuc);
                    db.store(bcUpdate);
                    
                    tbmodel.getDataVector().removeAllElements();
                    LoadAllData();
                    jtbBuuCuc.setRowSelectionInterval(index, index);
                    JOptionPane.showMessageDialog(this,"Da cap nhat "+tenBuuCuc);
                }
            }
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Cap nhat khong thanh cong, Error:"+ex);
            }
            
        }
    }//GEN-LAST:event_btnLuuActionPerformed

